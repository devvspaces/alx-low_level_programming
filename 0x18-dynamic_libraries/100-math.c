

/**
 * add - add a and b
 *
 * @a: a
 * @b: b
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub a and b
 *
 * @a: a
 * @b: b
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul a and b
 *
 * @a: a
 * @b: b
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div a and b
 *
 * @a: a
 * @b: b
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod a and b
 *
 * @a: a
 * @b: b
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
